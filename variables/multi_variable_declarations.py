#we save space by declaring multiple variables on the same line

sword_name, sword_damage, sword_length = "Excalibur", 10, 200

print(sword_name)
print(sword_damage)
print(sword_length)

#Any number of variables can be declared on the same line, but the convention is they should be variables that are related
#to eachother to keep things readable and easy to understand. This is a clean code best practice.

